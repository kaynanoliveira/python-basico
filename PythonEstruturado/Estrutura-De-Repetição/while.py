palavra = input('Entre com uma palavra: \n ')
while palavra != 'sair':
    palavra = input('Digite sair para encerrar o laço: \n')
print('Você digitou sair e agora está fora do laço')
#A linha 1 representa a solicitação ao usuário para que ele insira uma palavra a ser armazenada na variável palavra;
#A linha 2 cria o laço while, que depende da condição <valor da variável palavra ser diferente de ‘sair’>;
#A linha 3 será repetida enquanto a condição for verdadeira, ou seja, enquanto o valor da variável palavra for diferente de ‘sair’. Quando esses valores forem iguais, a condição do laço while será falsa e o laço, encerrado;
#A linha 4 representa a impressão da mensagem fora do laço while.

#No laço while, a condição é testada antes da iteração.
#O laço while testa e executa caso a condição seja verdadeira.